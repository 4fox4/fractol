#include "libft.h"

double		ft_sqrt(float n)
{
	float	s;
	double	d;
	double	x;

	s = 1;
	while (s * s <= n)
		s++;
	s--;
	d = 0.001;
	while (d < 1.0)
	{
		x = s + d;
		if (x * x > n)
		{
			x -= 0.001;
			break ;
		}
		d += 0.001;
	}
	return (x);
}
